STRAIN_TF_DB_PATH = Path(__file__).parent / "strain_tf_database.csv"


def load_strain_tf_database(db_path: Path = STRAIN_TF_DB_PATH) -> dict[str, list[str]]:
    """Load strain_tf_database.csv and return {group_id: [tf, ...]} mapping.

    The CSV has columns: group_id, channel, tf. A single group_id can have
    multiple rows (one per channel, e.g. GFP and mCherry), so each key maps
    to a list of TF names rather than a single string.
    """
    db: dict[str, list[str]] = {}
    if not db_path.exists():
        return db
    with open(db_path, newline="") as f:
        for row in csv.DictReader(f):
            gid = row.get("group_id", "").strip()
            tf = row.get("tf", "").strip()
            if gid and tf:
                db.setdefault(gid, [])
                if tf not in db[gid]:  # avoid duplicates when multiple channels share a TF
                    db[gid].append(tf)
    return db


def lookup_tfs_from_strains(strain_ids: list[str], db: dict[str, list[str]]) -> list[str]:
    """Return TF names for the given strain/group IDs from the database.

    Used for older experiments where the group ID is a plain number (e.g. 898)
    rather than an explicit TF name in the group label.
    """
    tfs = []
    for sid in strain_ids:
        tfs.extend(db.get(sid, []))  # returns [] if strain not in db
    return _dedupe_preserve_order(tfs)


def normalize_tf_name(name: str) -> str:
    """Normalise a gene name to standard yeast title-case (e.g. MSN2 → Msn2, REI1 → Rei1)."""
    if not name or name.upper() in ("UNKNOWN", ""):
        return name
    # First letter uppercase, remaining letters lowercase; digits are preserved.
    return name[0].upper() + name[1:].lower()


def parse_tf_from_dataset_name(dataset_name: str) -> list[str]:
    """Extract known TF names from an OMERO dataset name string.

    Handles common Swain-lab naming patterns:
    - CamelCase concatenation: ``Msn2Dot6Mig1``  (split at digit→uppercase junctions)
    - Underscore tokens:       ``Switch_2to0pGlc_Msn2_dIra12_00``
    - Uppercase gene tokens:   ``GAL3_MAN_GAL_00``

    Strategy: split name on ``_``, ``-``, and spaces to get coarse tokens; for each
    token attempt a direct (case-insensitive) lookup first, then re-segment by
    extracting CamelCase sub-words ([A-Z][a-z0-9]+) and look those up individually.
    """
    tf_lookup = {tf.lower(): tf for tf in KNOWN_TFS}
    found = []
    for token in re.split(r'[_\-\s]', dataset_name):
        if not token:
            continue
        # Direct lookup — handles plain tokens like "Msn2", "GAL3", "Mig1"
        if token.lower() in tf_lookup:
            found.append(tf_lookup[token.lower()])
            continue
        # CamelCase segmentation — splits "Msn2Dot6Mig1" → ["Msn2", "Dot6", "Mig1"].
        # Pattern [A-Z][a-z0-9]+ matches one uppercase letter followed by lowercase/digits,
        # which is the standard yeast gene-name segment (Msn, Dot, Mig + trailing digits).
        for sub in re.findall(r'[A-Z][a-z0-9]+', token):
            if sub.lower() in tf_lookup:
                found.append(tf_lookup[sub.lower()])
    return _dedupe_preserve_order(found)


def parse_tf_from_strain_text(log_text: str) -> list[str]:
    """Extract known TF names from 'Protein-FluorescentTag' constructs in log text.

    Scans for patterns like ``Msn2-GFP``, ``Dot6-mCherry``, filters by KNOWN_TFS.
    This supplements group-label and strain-DB lookups for older free-text logs.
    """
    tf_lookup = {tf.lower(): tf for tf in KNOWN_TFS}
    found = []
    for m in _TAGGED_PROTEIN_PATTERN.finditer(log_text):
        protein = normalize_tf_name(m.group(1))
        canonical = tf_lookup.get(protein.lower())
        if canonical:
            found.append(canonical)
    return _dedupe_preserve_order(found)


_CONDITION_SYSTEM_PROMPT = """\
You are extracting the experimental condition from a microscopy experiment log.
The condition describes what changed during the experiment (e.g. a media switch).

Return the condition as a concise "X to Y" phrase on a single line, for example:
  2% glucose to 0% glucose
  high nitrogen to low nitrogen
  2% glucose to 0.1% glucose

If no condition change is described, return exactly: UNKNOWN
Return only the condition phrase or UNKNOWN — no other text.
"""


def parse_condition(
    log_text: str,
    client: "openai.OpenAI | None" = None,
    model: str = MODEL,
) -> str:
    """Extract the experimental condition from the 'Experiment details' section.

    Strategy:
    1. Locate the "Experiment details:" line and extract its content.
    2. Search for a "switch from X to Y" phrase and return "X to Y" directly.
       Example: "Switch from 2% glucose to 0%." → "2% glucose to 0%"
    3. If no switch phrase is found and an LLM client is provided, ask the LLM
       to interpret the free-text details and extract the condition.
    4. Last resort: return the first sentence of the details (capped at 200 chars).
    """
    # Step 1: extract the experiment details block
    m = _EXP_DETAILS_PATTERN.search(log_text)
    if not m:
        return ""
    details = m.group(1).strip()
    if not details:
        return ""

    # Step 2: fast regex path — "switch from X to Y" is unambiguous
    sw = _SWITCH_CONDITION_PATTERN.search(details)
    if sw:
        from_part = sw.group(1).strip().rstrip(".,; ")  # e.g. "2% glucose"
        to_part = sw.group(2).strip().rstrip(".,; ")    # e.g. "0%"
        return f"{from_part} to {to_part}"

    # Step 3: LLM fallback — regex found no switch phrase; ask the LLM to
    # interpret the free-text experiment details and extract the condition.
    if client is not None:
        try:
            response = client.chat.completions.create(
                model=model,
                max_completion_tokens=40,  # condition phrase is short
                timeout=OPENAI_TIMEOUT_SECONDS,
                messages=[
                    {"role": "system", "content": _CONDITION_SYSTEM_PROMPT},
                    # Send only the experiment details text, not the whole log
                    {"role": "user", "content": details[:500]},
                ],
            )
            llm_condition = response.choices[0].message.content.strip()
            if llm_condition and llm_condition.upper() != "UNKNOWN":
                return llm_condition
        except Exception:
            pass  # LLM call failed; fall through to last resort

    # Step 4: last resort — no switch phrase and no LLM; return first sentence
    first_sentence = re.split(r'(?<=[.!?])\s', details)[0]
    return first_sentence[:200]


_TF_SYSTEM_PROMPT = """\
You are extracting transcription factor (TF) names from a yeast live-cell microscopy experiment log.
Return a comma-separated list of TF names exactly as they appear in yeast nomenclature (e.g. Msn2, Mig1, Rei1).
If no TF is identifiable, return exactly: UNKNOWN
Return only the comma-separated list or UNKNOWN — no other text.
"""


def parse_tf_from_groups(
    log_text: str,
    dataset_name: str = "",
    strain_ids: list[str] | None = None,
    strain_db: dict[str, list[str]] | None = None,
    client: "openai.OpenAI | None" = None,
    model: str = MODEL,
) -> list[str]:
    """Identify TF names imaged in the experiment using a five-level fallback.

    Priority (highest → lowest confidence):
    1. Batgirl group labels — ``group: ch1_REI1`` → deterministic, no ambiguity.
    2. Strain DB (strain_tf_database.csv) — numeric group IDs mapped to TF names.
    3. Dataset name — extract KNOWN_TFS names from the OMERO dataset name string.
    4. Tagged-protein patterns — ``Protein-GFP`` / ``Protein-mCherry`` in log text,
       filtered against KNOWN_TFS.
    5. LLM fallback — only when all deterministic paths yield nothing.
    """
    # Step 1: Batgirl group-label regex — normalise to title-case (REI1 → Rei1)
    raw_tfs = _GROUP_TF_PATTERN.findall(log_text)
    if raw_tfs:
        return _dedupe_preserve_order(normalize_tf_name(tf) for tf in raw_tfs)

    # Step 2: strain DB lookup — authoritative source for older numeric group IDs
    if strain_ids and strain_db:
        tfs = lookup_tfs_from_strains(strain_ids, strain_db)
        if tfs:
            return tfs

    # Step 3: dataset name parsing — many names encode the TF(s) explicitly,
    # e.g. "Ramp_2to0pGlc_Msn2Dot6Mig1_00" or "GAL3_MAN_GAL_00"
    if dataset_name:
        tfs = parse_tf_from_dataset_name(dataset_name)
        if tfs:
            return tfs

    # Step 4: tagged-protein patterns in log text — "Protein-GFP" / "Protein-mCherry"
    if log_text:
        tfs = parse_tf_from_strain_text(log_text)
        if tfs:
            return tfs

    # Step 5: LLM fallback — only when all deterministic paths are exhausted
    if client is not None:
        try:
            response = client.chat.completions.create(
                model=model,
                max_completion_tokens=60,
                timeout=OPENAI_TIMEOUT_SECONDS,
                messages=[
                    {"role": "system", "content": _TF_SYSTEM_PROMPT},
                    {"role": "user", "content": log_text[:1000]},
                ],
            )
            llm_tfs = response.choices[0].message.content.strip()
            if llm_tfs and llm_tfs.upper() != "UNKNOWN":
                return _dedupe_preserve_order(t.strip() for t in llm_tfs.split(",") if t.strip())
        except Exception:
            pass

    return ["UNKNOWN"]


def parse_yst_strain_ids(log_text: str) -> list[str]:
    """Extract numeric IDs from YST_XXXX references in the log text.

    Strains are written as e.g. "YST_1490" in experiment details or comments.
    Returns just the number part ("1490") so it can be looked up in the
    strain_tf_database.
    """
    return _dedupe_preserve_order(_YST_STRAIN_PATTERN.findall(log_text))


def parse_timepoints(log_text: str) -> int | None:
    """Return the number of acquired timepoints parsed from the log, or None if not found."""
    m = _ACQ_TIME_SETTINGS_PATTERN.search(log_text)
    if m:
        return int(m.group(1))
    for pattern in _TIMEPOINTS_PATTERNS:
        m = pattern.search(log_text)
        if m:
            return int(m.group(1))
    indexed_timepoints = [int(m.group(1)) for m in _TIMEPOINT_INDEX_PATTERN.finditer(log_text)]
    if indexed_timepoints:
        return max(indexed_timepoints)
    return None


def parse_strain_ids(log_text: str) -> list[str]:
    """Return strain/group IDs found in log or acquisition metadata.

    Captures numeric group IDs (e.g. ``group: 898``), YST_XXXX numeric parts,
    explicit ``Strain:`` fields, and old-format acquisition point groups.
    """
    values = _STRAIN_PATTERN.findall(log_text)
    values.extend(parse_yst_strain_ids(log_text))
    m = _STRAIN_DETAILS_PATTERN.search(log_text)
    if m:
        values.extend(part.strip() for part in m.group(1).split(","))
    values.extend(parse_point_groups(log_text))
    return _dedupe_preserve_order(values)


def parse_acq_channels(text: str) -> list[str]:
    """Return channel names from an old multiDGUI `Channels:` table."""
    m = re.search(r'(?ims)^(?:.*Acq\.txt\s+)?Channels:\s*\n(.*?)(?:\n\s*\n|^Z_sectioning:|^Time_settings:|^Points:)', text)
    if not m:
        return []
    channels = []
    for row in csv.reader(m.group(1).splitlines(), skipinitialspace=True):
        if not row:
            continue
        name = row[0].strip()
        if not name or name.lower().startswith("channel name"):
            continue
        channels.append(name)
    return _dedupe_preserve_order(channels)


def parse_image_config_channels(text: str) -> list[str]:
    """Return channel/config names from newer `Image Configs` CSV blocks."""
    channels = []
    lines = text.splitlines()
    for i, line in enumerate(lines):
        if not line.lower().startswith("image config,channel,"):
            continue
        for data_line in lines[i + 1:]:
            if not data_line.strip():
                break
            row = next(csv.reader([data_line], skipinitialspace=True), [])
            if len(row) < 2:
                continue
            channels.extend([row[0].strip(), row[1].strip()])
    return _dedupe_preserve_order(channels)


def parse_setup_channels(text: str) -> list[str]:
    """Return channel section names from `Microscope setup for used channels` blocks."""
    m = re.search(
        r'(?ims)^Microscope setup for used channels:\s*\n(.*?)(?:^Micromanager config file:|^Omero project:|^Experiment started at:)',
        text,
    )
    if not m:
        return []
    return _dedupe_preserve_order(
        match.group(1).strip()
        for match in re.finditer(r'(?m)^\s*([A-Za-z][\w-]*)\s*:\s*$', m.group(1))
    )


def parse_runtime_channels(text: str) -> list[str]:
    """Return channels mentioned in runtime acquisition lines."""
    return _dedupe_preserve_order(
        channel for channel in _CHANNEL_LOG_PATTERN.findall(text) if channel.lower() != "name"
    )


def parse_channels(text: str) -> list[str]:
    """Return all channel names found across old and new metadata formats."""
    channels = []
    channels.extend(parse_acq_channels(text))
    channels.extend(parse_image_config_channels(text))
    channels.extend(parse_setup_channels(text))
    channels.extend(parse_runtime_channels(text))
    return _dedupe_preserve_order(channels)


def fluorescence_channels(channels: list[str]) -> list[str]:
    """Return channels that are known fluorescence channels."""
    fluorescence = []
    known = {channel.lower(): channel for channel in FLUORESCENCE_CHANNELS}
    for channel in channels:
        lower_channel = channel.lower()
        if lower_channel in ("brightfield", "brightfield1"):
            continue
        if lower_channel in known:
            fluorescence.append(known[lower_channel])
            continue
        for known_lower, known_name in known.items():
            if known_lower in lower_channel:
                fluorescence.append(known_name)
                break
    return _dedupe_preserve_order(fluorescence)


def parse_point_groups(text: str) -> list[str]:
    """Return old acquisition point-group labels such as `group 1: by4741`."""
    m = re.search(r'(?ims)^Points:\s*\n(.*?)(?:\n\s*\n|^Flow_control:|^Dynamic flow details:)', text)
    if not m:
        return []
    rows = list(csv.reader(m.group(1).splitlines(), skipinitialspace=True))
    if not rows:
        return []
    header = [col.strip().lower() for col in rows[0]]
    if "position name" not in header or "group" not in header:
        return []
    name_idx = header.index("position name")
    group_idx = header.index("group")
    groups: dict[str, list[str]] = {}
    for row in rows[1:]:
        if len(row) <= max(name_idx, group_idx):
            continue
        position_name = row[name_idx].strip()
        group = row[group_idx].strip()
        prefix = re.sub(r'_\d+$', '', position_name).strip()
        if not group or not prefix:
            continue
        groups.setdefault(group, [])
        if prefix not in groups[group]:
            groups[group].append(prefix)
    return [f"group {group}: {'/'.join(names)}" for group, names in groups.items()]


