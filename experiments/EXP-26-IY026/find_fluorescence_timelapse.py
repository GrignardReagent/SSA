@dataclass
class MetadataAnnotations:
    """Text metadata attached to one OMERO dataset."""

    log_text: str | None = None
    acq_text: str | None = None
    log_filename: str = ""
    acq_filename: str = ""
    truncated_files: str = ""

    @property
    def has_metadata(self) -> bool:
        return self.log_text is not None or self.acq_text is not None

    def combined_text(self) -> str | None:
        parts = []
        if self.log_text:
            parts.append(f"Log file ({self.log_filename or 'unknown'}):\n{self.log_text}")
        if self.acq_text:
            parts.append(f"Acquisition file ({self.acq_filename or 'unknown'}):\n{self.acq_text}")
        return "\n\n".join(parts) if parts else None


def _read_file_annotation_text(ann, max_bytes: int = MAX_ANNOTATION_BYTES) -> tuple[str, bool]:
    """Read up to `max_bytes` from an OMERO file annotation."""
    chunks = []
    total_bytes = 0
    truncated = False
    for chunk in ann.getFileInChunks():
        if total_bytes + len(chunk) > max_bytes:
            keep = max_bytes - total_bytes
            if keep > 0:
                chunks.append(chunk[:keep])
            truncated = True
            break
        chunks.append(chunk)
        total_bytes += len(chunk)
    return b"".join(chunks).decode("utf-8", errors="ignore"), truncated

def connect_omero(host: str, username: str, password: str) -> BlitzGateway:
    conn = BlitzGateway(host=host, username=username, passwd=password)
    if not conn.connect():
        raise ConnectionError(f"Could not connect to OMERO at {host}")
    conn.c.enableKeepAlive(60)  # ping the server every 60 s so long runs don't get disconnected
    return conn


def get_all_datasets(conn: BlitzGateway) -> list[tuple[int, str]]:
    """Return (id, name) for every dataset visible to the connected user."""
    return [(int(ds.getId()), ds.getName()) for ds in conn.getObjects("Dataset")]


_MICROSCOPY_LOG_HEADER = "Swain Lab microscope experiment log file"
_MICROSCOPY_LOG_KEYWORDS = ("Experiment details", "Acquisition settings", "Microscope name")
_ACQ_FILE_KEYWORDS = ("Channels:", "Time_settings:", "Points:")


def _is_microscopy_log(text: str) -> bool:
    return _MICROSCOPY_LOG_HEADER in text or any(k in text for k in _MICROSCOPY_LOG_KEYWORDS)


def _is_acquisition_file(fname: str, text: str) -> bool:
    return fname.endswith("acq.txt") or sum(k in text for k in _ACQ_FILE_KEYWORDS) >= 2


def read_metadata_annotations(dataset_obj) -> MetadataAnnotations:
    """
    Return decoded microscopy metadata annotations attached to the dataset.

    Old multiDGUI datasets can attach two separate files: a `*log.txt` file
    with experiment details and a `*Acq.txt` file with acquisition settings.
    Newer datasets often put the acquisition settings inside a single `.log`.
    """
    metadata = MetadataAnnotations()
    log_candidates: list[tuple[int, str, str]] = []  # (priority, filename, text)
    acq_candidates: list[tuple[int, str, str]] = []
    for ann in dataset_obj.listAnnotations():
        if not hasattr(ann, "getFileName"):
            continue  # skip non-file annotations (tags, comments, etc.)
        fname = (ann.getFileName() or "").lower()
        if not (fname.endswith(".log") or fname.endswith(".txt")):
            continue  # only consider plain-text / log files
        text, truncated = _read_file_annotation_text(ann)
        if truncated:
            metadata.truncated_files = ";".join(
                value for value in (metadata.truncated_files, fname) if value
            )
        if _is_acquisition_file(fname, text):
            acq_candidates.append((1 if fname.endswith("acq.txt") else 2, fname, text))
            continue
        if _is_microscopy_log(text):
            log_candidates.append((1, fname, text))
            continue
        if fname.endswith("log.txt"):
            log_candidates.append((2, fname, text))   # older multiDGUI naming convention
        elif fname.endswith(".log"):
            log_candidates.append((3, fname, text))   # newer Batgirl format
        else:
            log_candidates.append((4, fname, text))   # last resort: any .txt file
    if log_candidates:
        _, metadata.log_filename, metadata.log_text = sorted(log_candidates, key=lambda x: x[0])[0]
    if acq_candidates:
        _, metadata.acq_filename, metadata.acq_text = sorted(acq_candidates, key=lambda x: x[0])[0]
    return metadata


# ---------------------------------------------------------------------------
# Log parsing helpers (deterministic, no LLM)
# ---------------------------------------------------------------------------

FLUORESCENCE_CHANNELS = (
    "GFP",
    "GFP_Z",
    "GFPFast",
    "mCherry",
    "mCherry2",
    "mCherry_Z",
    "Citrine",
    "Flavin",
    "mKO2",
    "Cy5",
    "cy5",
    "pHluorin405",
    "pHluorin488",
    "NADH",
    "mTurquoise2",
    "tdTomatoFRET",
)

# Ordered by specificity; first match wins
_TIMEPOINTS_PATTERNS = [
    re.compile(r'number\s+of\s+timepoints\s*[=:]\s*(\d+)', re.IGNORECASE),
    re.compile(r'ntimepoints\s*[=:]\s*(\d+)', re.IGNORECASE),
    re.compile(r'^frames\s*:\s*(\d+)', re.IGNORECASE | re.MULTILINE),
    re.compile(r'\btime\s*point\s+\d+\s*(?:/|of)\s*(\d+)', re.IGNORECASE),
    re.compile(r'\btimepoint\s+\d+\s*(?:/|of)\s*(\d+)', re.IGNORECASE),
    re.compile(r'\bframes?\s+\d+\s*(?:/|of)\s*(\d+)', re.IGNORECASE),
]

_ACQ_TIME_SETTINGS_PATTERN = re.compile(
    r'(?im)^Time_settings:\s*\n\s*[^,\n]+,\s*[^,\n]+,\s*(\d+)\s*,'
)

_TIMEPOINT_INDEX_PATTERN = re.compile(
    r'^\s*-*\s*time\s*points?[_\s:=]*(\d+)\b',
    re.IGNORECASE | re.MULTILINE,
)

_STRAIN_PATTERN = re.compile(r'^group\s*:\s*(\d+)', re.IGNORECASE | re.MULTILINE)
_STRAIN_DETAILS_PATTERN = re.compile(
    r'Experiment details:.*?\bStrain:\s*(.*?)(?:\s+Comments:|\n)',
    re.IGNORECASE | re.DOTALL,
)
_CHANNEL_LOG_PATTERN = re.compile(
    r'(?im)^\s*(?:Channel(?: configuration set to)?|Image config)\s*:?\s*([A-Za-z][\w-]*)'
)


def _dedupe_preserve_order(values: list[str]) -> list[str]:
    seen = set()
    unique = []
    for value in values:
        cleaned = value.strip()
        if not cleaned or cleaned.lower() in seen:
            continue
        seen.add(cleaned.lower())
        unique.append(cleaned)
    return unique


def parse_timepoints(log_text: str) -> int | None:
    """Return the number of acquired timepoints parsed from the log, or None if not found."""
    m = _ACQ_TIME_SETTINGS_PATTERN.search(log_text)
    if m:
        return int(m.group(1))
    for pattern in _TIMEPOINTS_PATTERNS:
        m = pattern.search(log_text)
        if m:
            return int(m.group(1))
    indexed_timepoints = [int(m.group(1)) for m in _TIMEPOINT_INDEX_PATTERN.finditer(log_text)]
    if indexed_timepoints:
        return max(indexed_timepoints)
    return None


def parse_strain_ids(log_text: str) -> list[str]:
    """Return strain/group labels found in log or acquisition metadata."""
    values = _STRAIN_PATTERN.findall(log_text)
    m = _STRAIN_DETAILS_PATTERN.search(log_text)
    if m:
        values.extend(part.strip() for part in m.group(1).split(","))
    values.extend(parse_point_groups(log_text))
    return _dedupe_preserve_order(values)


def parse_acq_channels(text: str) -> list[str]:
    """Return channel names from an old multiDGUI `Channels:` table."""
    m = re.search(r'(?ims)^(?:.*Acq\.txt\s+)?Channels:\s*\n(.*?)(?:\n\s*\n|^Z_sectioning:|^Time_settings:|^Points:)', text)
    if not m:
        return []
    channels = []
    for row in csv.reader(m.group(1).splitlines(), skipinitialspace=True):
        if not row:
            continue
        name = row[0].strip()
        if not name or name.lower().startswith("channel name"):
            continue
        channels.append(name)
    return _dedupe_preserve_order(channels)


def parse_image_config_channels(text: str) -> list[str]:
    """Return channel/config names from newer `Image Configs` CSV blocks."""
    channels = []
    lines = text.splitlines()
    for i, line in enumerate(lines):
        if not line.lower().startswith("image config,channel,"):
            continue
        for data_line in lines[i + 1:]:
            if not data_line.strip():
                break
            row = next(csv.reader([data_line], skipinitialspace=True), [])
            if len(row) < 2:
                continue
            channels.extend([row[0].strip(), row[1].strip()])
    return _dedupe_preserve_order(channels)


def parse_setup_channels(text: str) -> list[str]:
    """Return channel section names from `Microscope setup for used channels` blocks."""
    m = re.search(
        r'(?ims)^Microscope setup for used channels:\s*\n(.*?)(?:^Micromanager config file:|^Omero project:|^Experiment started at:)',
        text,
    )
    if not m:
        return []
    return _dedupe_preserve_order(
        match.group(1).strip()
        for match in re.finditer(r'(?m)^\s*([A-Za-z][\w-]*)\s*:\s*$', m.group(1))
    )


def parse_runtime_channels(text: str) -> list[str]:
    """Return channels mentioned in runtime acquisition lines."""
    return _dedupe_preserve_order(
        channel for channel in _CHANNEL_LOG_PATTERN.findall(text) if channel.lower() != "name"
    )


def parse_channels(text: str) -> list[str]:
    """Return all channel names found across old and new metadata formats."""
    channels = []
    channels.extend(parse_acq_channels(text))
    channels.extend(parse_image_config_channels(text))
    channels.extend(parse_setup_channels(text))
    channels.extend(parse_runtime_channels(text))
    return _dedupe_preserve_order(channels)


def fluorescence_channels(channels: list[str]) -> list[str]:
    """Return channels that are known fluorescence channels."""
    fluorescence = []
    known = {channel.lower(): channel for channel in FLUORESCENCE_CHANNELS}
    for channel in channels:
        lower_channel = channel.lower()
        if lower_channel in ("brightfield", "brightfield1"):
            continue
        if lower_channel in known:
            fluorescence.append(known[lower_channel])
            continue
        for known_lower, known_name in known.items():
            if known_lower in lower_channel:
                fluorescence.append(known_name)
                break
    return _dedupe_preserve_order(fluorescence)


def parse_point_groups(text: str) -> list[str]:
    """Return old acquisition point-group labels such as `group 1: by4741`."""
    m = re.search(r'(?ims)^Points:\s*\n(.*?)(?:\n\s*\n|^Flow_control:|^Dynamic flow details:)', text)
    if not m:
        return []
    rows = list(csv.reader(m.group(1).splitlines(), skipinitialspace=True))
    if not rows:
        return []
    header = [col.strip().lower() for col in rows[0]]
    if "position name" not in header or "group" not in header:
        return []
    name_idx = header.index("position name")
    group_idx = header.index("group")
    groups: dict[str, list[str]] = {}
    for row in rows[1:]:
        if len(row) <= max(name_idx, group_idx):
            continue
        position_name = row[name_idx].strip()
        group = row[group_idx].strip()
        prefix = re.sub(r'_\d+$', '', position_name).strip()
        if not group or not prefix:
            continue
        groups.setdefault(group, [])
        if prefix not in groups[group]:
            groups[group].append(prefix)
    return [f"group {group}: {'/'.join(names)}" for group, names in groups.items()]


